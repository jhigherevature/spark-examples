package datatypes;

public class DatatypeBasics {
	public static void main(String[] args) {
		int i = 36;
		double d = 3.14;
		System.out.println(i); // 36
		System.out.println(d); // 3.14
		
		char c = 'A';
		System.out.println(c);
		
		boolean b = true;
		System.out.println(b);
		
		byte by = 127;
		System.out.println(by);
	}
}
