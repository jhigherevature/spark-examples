package a_basics;

public class B_MathematicalOperators {
	public static void main(String[] args) {
		/** 1. explain what an operator and operands are */
			// assignment operator
			int myNum = 5;
			
		/** 2. explain mathematical operators: */
			// addition operator
			System.out.println(5 + 1); // 6
			
			// subtraction operator
			System.out.println(5 - 1); // 4
			
			// multiplication operator
			System.out.println(5 * 5); // 25
		
			// division operator
			System.out.println(15 / 5); // 3
			
			// modulo operator
			System.out.println(12 % 4); // 0
			System.out.println(12 % 5); // 2
			
		/** 3. (optional) explain mathematical operators using variables: */
			int variable = 5;
			System.out.println(variable + 20); // 25
			System.out.println(variable); // still 5
			
	}
}
